"""***************************************************************************************************************************************
Rotate a Square Matrix 90 Degree Clockwise
Input:
1    2   3
4   5   6
7   8   9
Output:
7   4   1
8   5   2
9   6   3
******************************************************************************************************************************************"""
matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]
print(list(zip(*matrix)))
transposed_matrix = list(zip(*matrix))

for row in transposed_matrix:
    print(*row)
